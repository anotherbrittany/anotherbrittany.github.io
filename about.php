<p>While my family sleeps peacefully each night, I patiently wait for my AOL dialup to connect to the Internet -- the entire time, hoping my parents don’t wake up. As soon as I hear that familiar voice telling me I’ve got mail, my heart flutters; I know I’m now free to surf HTMLgoodies.com until the sun comes up, teaching myself basic HTML in Notepad.</p>

