<h3>LEARNING TO CODE WAS A SLOW PROCESS.</h3>

<p>Very slow, although it never felt painfully slow. I was driven, not discouraged, but I also didn’t know many other coders. </p>

<p><em>Were there any female web designers? Were there any programmers my own age? Were there any in Kentucky, in the South, in the Midwest? Would there ever be?</em></p>

